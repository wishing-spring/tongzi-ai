def get_anchors(star_name: str) -> Optional[list[int]]:
    if star_name in ANCHORS:
        return ANCHORS[star_name]
    clean = star_name.replace("_chem", "").replace("_life", "").rstrip("_")
    if clean in ANCHORS:
        return ANCHORS[clean]
    return None

def is_anchored(star_name: str, slot_idx: int) -> bool:
    a = get_anchors(star_name)
    return slot_idx in a if a else False

def anchored_score(star_name: str, slot_idx: int) -> float:
    a = get_anchors(star_name)
    if a is None:
        return 0.0
    if slot_idx == a[0]:
        return 1.5
    return 1.0 if slot_idx in a else 0.0

def anchor_stats() -> dict:
    total = len(ANCHORS)
    lens = [len(v) for v in ANCHORS.values()]
    avg = sum(lens) / total if total else 0
    return {"total_anchored": total, "avg_per_star": round(avg, 1)}
