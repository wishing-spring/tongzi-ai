# ============================================================
# 注入核心
# ============================================================
def 注入种子(core: TongziCore):
    """将本源种子卦象注入赤子心内丹"""
    for tag, vec in 种子库:
        core.data[tag] = vec
        core.active[tag] = 0     # 种子初始tick=0，永不归元
        core.hits[tag] = 0
        core.potency[tag] = 0
    return len(种子库)

