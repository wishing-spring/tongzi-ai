class GuaZaiCeng:
    """数据挂载层——血肉依附骨架"""

    def __init__(self, core: TongziCore, mao: ShiErMao):
        self.core = core
        self.mao = mao
        self.意象库 = 意象库
        self.顺向链路 = 顺向链路
        self.情态链路 = 情态链路
        self.译出规则 = 译出规则

    # ── 意象匹配 ──

    def 查意象(self, word: str) -> Optional[dict]:
        """查单个词的意象定义"""
        return self.意象库.get(word)

    def 提取意象词(self, text: str) -> list[str]:
        """从文本中提取匹配意象库的词（最长匹配优先）"""
        found = []
        i = 0
        while i < len(text):
            matched = None
            # 最长匹配
            for length in range(min(10, len(text) - i), 0, -1):
                sub = text[i:i + length]
                if sub in self.意象库:
                    matched = sub
                    break
            if matched:
                found.append(matched)
                i += len(matched)
            else:
                i += 1
        return found

    def 词的意象类(self, word: str) -> str:
        """返回词的意象类别"""
        yi = self.意象库.get(word)
        return yi["类"] if yi else "未知"

    # ── 意念链路 ──

    def 顺接句类(self, current_type: int) -> list[int]:
        """给定当前句类，返回自然顺接的下一个句类列表"""
        return self.顺向链路.get(current_type, [0])

    def 顺接情态(self, current_sentiment: int) -> list[int]:
        """给定当前情态极性，返回自然顺接的情态列表"""
        return self.情态链路.get(current_sentiment, [0])

    # ── 簇态势 → 匹配最佳译出规则 ──

    def 评估态势(self, clues: list[str]) -> dict:
        """根据仓储状态评估当前簇态势"""
        n = self.core.size
        active_n = self.core.active_count
        active_ratio = active_n / max(n, 1)

        return {
            "密度": "高" if n > 80 else ("中" if n > 20 else "低"),
            "活跃比": "高" if active_ratio > 0.7 else ("中" if active_ratio > 0.3 else "低"),
            "阳锚": self.mao.阳活跃数(),
            "阴锚": self.mao.阴活跃数(),
            "相似团数": 0,  # 由调用方填入
        }

    def 匹配译出规则(self, 态势: dict) -> str:
        """从规则库中匹配最佳模板"""
        最佳规则 = None
        最佳分 = -1

        for 条件, 模板 in self.译出规则:
            if not 条件:
                continue
            分 = 0
            for k, v in 条件.items():
                if k in 态势 and 态势[k] == v:
                    分 += 1
            if 分 > 最佳分:
                最佳分 = 分
                最佳规则 = 模板

        if 最佳规则 is None:
            最佳规则 = "{词1}。"

        return 最佳规则

    def 生成应答(self, input_text: str) -> str:
        """核心：输入一句话，输出最朴素的应答。"""
        # 1. 提取输入中的意象词
        clues = self.提取意象词(input_text)

        # 2. 编码输入文本并入库（丹体纳新）
        self.core.add(f"输入_{self.core.tick}", input_text)
        self.mao.tick_cycle()

        # 3. 评估当前态势
        态势 = self.评估态势(clues)

        # 4. 找相似团（聚锚）
        if clues:
            目标向量 = self.core.encode_text(clues[0])
            相似 = self.core.find_similar(目标向量, threshold=4)
            态势["相似团数"] = max(1, len(相似))

        # 5. 匹配译出模板
        模板 = self.匹配译出规则(态势)

        # 6. 填充模板
        if clues:
            词1 = clues[0] if len(clues) >= 1 else ""
            词2 = clues[1] if len(clues) >= 2 else ""
        else:
            词1, 词2 = "", ""

        应答 = 模板.replace("{词1}", 词1).replace("{词2}", 词2)
        # 清理多余的标点占位
        应答 = 应答.replace("。", "。").replace("。。", "。").strip()
        if not 应答 or 应答 == "。":
            应答 = "嗯。"

        return 应答

